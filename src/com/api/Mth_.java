package com.api;

public class Mth_ {

	public static void main(String[] args) {
		System.out.println("计算绝对值的结果是:"+Math.abs(-1));
		System.out.println("计算正弦的结果是:"+Math.sin(1.57));
		System.out.println("计算余弦的结果是:"+Math.cos(2.0));
		System.out.println("计算正切的结果是:"+Math.tan(0.8));
		System.out.println("计算平方根的结果是:"+Math.sqrt(4));
		System.out.println("计算立方根的结果是:"+Math.cbrt(9));
		System.out.println("计算乘方的结果是:"+Math.pow(2,2));
		System.out.println("计算大于参数的最小整数的结果是:"+Math.ceil(4.6));
		//ceil 天花板
		System.out.println("计算小于参数的最小整数的结果是:"+Math.floor(4.6));
		//floor地板
		System.out.println("对小数进行四舍五入后的结果:"+Math.round(4.6));
		System.out.println("求两个数的最大值是:"+Math.max(5.1,5.5));
		System.out.println("求两个数的最小值是:"+Math.min(5.1,5.5));
		System.out.println("生成一个大于等于0.0小于1.0的随机值:"+Math.random());
		
	}

}
